    public void loadStudentsIntoTable(ArrayList<Student> studentList) {
        // 1. Get the table model

        // 2. Clear existing rows
        model.setRowCount(0);

        // 3. Add each student as a row
        for (Student s : studentList) {
            Object[] row = {
                s.getStudentId(),
                s.getFullName(),
                s.getAge(),
                s.getGender(),
                s.getDepartment(),
                s.getGPA()
            };
            model.addRow(row);
        }
    }
